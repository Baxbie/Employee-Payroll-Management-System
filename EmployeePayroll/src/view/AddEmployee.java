    private void btnAttachImgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttachImgActionPerformed
        // TODO add your handling code here:
        try {
            JFileChooser chooser = new JFileChooser();
            chooser.showOpenDialog(null);
            File file = chooser.getSelectedFile();

            fileName = file.getAbsolutePath();

            ImageIcon imageIcon = new ImageIcon(new ImageIcon(fileName).getImage().getScaledInstance(lblImage.getWidth(), lblImage.getHeight(), Image.SCALE_DEFAULT));
            lblImage.setIcon(imageIcon);

            try {
                File image = new File(fileName);
                FileInputStream fix = new FileInputStream(image);
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                byte[] buf = new byte[1024];

                for (int number; (number = fix.read(buf)) != -1;) {
                    baos.write(buf, 0, number);
                }
                personImage = baos.toByteArray();

            } catch (Exception e) {

            }

        } catch (Exception e) {

        }
    }//GEN-LAST:event_btnAttachImgActionPerformed

