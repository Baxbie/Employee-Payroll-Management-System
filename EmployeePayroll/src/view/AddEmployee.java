        String dobDate = ((JTextField) dateDOB.getDateEditor().getUiComponent()).getText();
        String email = txtEmail.getText();
        String contact = txtContact.getText();
        String address1 = txtAddress1.getText();
        String address2 = txtAddress2.getText();
        String houseNo = txtHouseNo.getText();
        String postalCode = txtPostalCode.getText();
        String department = txtDepartment.getText();
        String designation = txtDesignation.getText();
        String dateHiredDate = ((JTextField) dateDateHired.getDateEditor().getUiComponent()).getText();
        String basicSal = txtBasicSal.getText();
        String jobTitle = txtJobTitle.getText();
        String status = txtStatus.getText();

        if (Validator.RegUpdValidation(fName,lName,dobDate,email,contact,address1,address2,houseNo,postalCode,department,designation,dateHiredDate,basicSal,jobTitle,status) && Validator.emailValidation(email)) {
            int confirmation = JOptionPane.showConfirmDialog(null, "Are you sure you want to add records?", "Add Records", JOptionPane.YES_NO_OPTION);
            if (confirmation == 0) {

                try {
                    String sql = "INSERT INTO staff_information (firstName, lastName, dob, email, contact, address1, address2, houseNo, postalCode, department, designation, dateHired, salary, jobTitle, status, image, gender) VALUES(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";

                    String gender = "";
                    if (rbnMale.isSelected() == true) {
                        gender = "Male";
                    } else if (rbnFemale.isSelected() == true) {
                        gender = "Female";
                    }

                    pst = con.prepareStatement(sql);
                    pst.setString(1, fName);
                    pst.setString(2, lName);
                    pst.setString(3, dobDate);
                    pst.setString(4, email);
                    pst.setString(5, contact);
                    pst.setString(6, address1);
                    pst.setString(7, address2);
                    pst.setString(8, houseNo);
                    pst.setString(9, postalCode);
                    pst.setString(10, department);
                    pst.setString(11, designation);
                    pst.setString(12, dateHiredDate);
                    pst.setString(13, basicSal);
                    pst.setString(14, jobTitle);
                    pst.setString(15, status);
                    pst.setBytes(16, personImage);
                    pst.setString(17, gender);

                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Data saved successfully", "Information", JOptionPane.INFORMATION_MESSAGE);

                    clearData();

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                } finally {

                    try {
                        rs.close();
                        pst.close();

                    } catch (Exception e) {

                    }
                }
            }
        }
